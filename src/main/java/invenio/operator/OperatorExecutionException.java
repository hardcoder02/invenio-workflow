package invenio.operator;

public class OperatorExecutionException extends Exception {

	public OperatorExecutionException() {
		super();
	}

	public OperatorExecutionException(String message, Throwable cause) {
		super(message, cause);
	}

	public OperatorExecutionException(String message) {
		super(message);
	}

	public OperatorExecutionException(Throwable cause) {
		super(cause);
	}
	
}

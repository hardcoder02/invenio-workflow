package invenio.wf;

public class RuntimeWorkflowException extends RuntimeException {

	public RuntimeWorkflowException() {
	}

	public RuntimeWorkflowException(String arg0) {
		super(arg0);
	}

	public RuntimeWorkflowException(Throwable arg0) {
		super(arg0);
	}

	public RuntimeWorkflowException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
